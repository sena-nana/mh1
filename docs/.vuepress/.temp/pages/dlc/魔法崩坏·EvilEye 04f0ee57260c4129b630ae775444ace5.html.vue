<p>拟神的身体并不是完整的改造，并不能像魔法少女一样对疾病和毒素具有完全的免疫，也因此拟神可以和更多种类的药物和魔法科技产生反应。这些药物和科技在没有特殊说明的情况下效果可以叠加</p>
<h2 id="陷入绝望的拟神" tabindex="-1"><a class="header-anchor" href="#陷入绝望的拟神" aria-hidden="true">#</a> 陷入绝望的拟神</h2>
<p>当拟神的SAN值归0时，玩家需要进行因果的检定，如果检定结果小于因果值，拟神便能够保持短暂的理性，并在下一个战斗轮或是简短的行动后再次检定，直至她的理智通过某种途径恢复或是完全疯狂。陷入绝望后，她会无差别的攻击在场的所有角色直至自己死亡，并且玩家会永远失去该角色的控制权，除非其他玩家在该角色存活时将其击晕并注射精神抑制剂。</p>
