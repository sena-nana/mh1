<p>禁忌：在6小时内重复使用会损失额外的1点SAN值，在使用β-污秽抑制剂的6小时内使用α-污秽抑制剂会损失额外的5点SAN值</p>
<h3 id="_5-β精神抑制剂" tabindex="-1"><a class="header-anchor" href="#_5-β精神抑制剂" aria-hidden="true">#</a> 5-β精神抑制剂</h3>
<p>使用对象: 拟神
效果: 能够使绝望的拟神恢复理智并恢复1点SAN值，但是在1小时内会因为嗜睡处于半睡半醒的状态，需要同伴辅助才能完成行动</p>
<p>禁忌：与酒精同用效果会增强：能够恢复3点理智，但是会陷入1小时的昏迷</p>
<p>5-β精神抑制剂通过皮下注射的方式迅速起效，其作用机理为抑制中枢神经系统神经元对5-羟色胺再摄取，从而恢复突触间隙5-羟色胺的浓度</p>
<p>这种药物曾作为精神治疗药物，在经过调整后，加大浓度制成了5-β精神抑制剂。作为临床实验对象的拟神并未表现出除嗜睡以外的明显不良反应，因其成本低廉，被各拟神研究机构作为常备处理方案</p>
