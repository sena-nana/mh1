<h3 id="魅力-app-骰3d6-结果乘5-即是魅力属性" tabindex="-1"><a class="header-anchor" href="#魅力-app-骰3d6-结果乘5-即是魅力属性" aria-hidden="true">#</a> 魅力（APP）：骰3D6，结果乘5，即是魅力属性</h3>
<p>魅力属性是魔法少女外貌，气质等具有吸引力要素的抽象化表示。具有高魅力的魔法少女更容易成为目光的焦点。</p>
<h3 id="年龄" tabindex="-1"><a class="header-anchor" href="#年龄" aria-hidden="true">#</a> 年龄</h3>
<p>魔法少女的年龄应在10－24之间，若设想的魔法少女超过这个范围，请和主持人说明原由并调整相关数值。对照相应年龄，调整魔法少女的属性</p>
<ul>
<li>10－12岁：力量和体质合计减5点，记忆减5点，对敏捷进行两次增强检定并取较好一次，感情加5点。</li>
<li>13－15岁：力量和体质合计减5点，对敏捷进行一次增强检定</li>
<li>16－18岁：对记忆进行一次增强检定</li>
<li>19－24岁：敏捷减5点。对记忆进行两次增强检定并取较好一次，感情减5点</li>
</ul>
<p>进行增强检定时，骰百分骰，若结果大于当前的属性数值，则给对应属性增加1d10点（但不会超过99）</p>
<h3 id="一半属性和五分之一属性" tabindex="-1"><a class="header-anchor" href="#一半属性和五分之一属性" aria-hidden="true">#</a> 一半属性和五分之一属性</h3>
<p>当确定好所有属性的百分比数值后，下一步就是计算属性的一半值和五分之一值，并将它们填入魔法少女卡对应属性百分骰旁边。计算方法为百分骰除以2或5，向下取整。
属性值全部填写完毕后，最大值为属性满值，属性半值和五分之一值所用到的地方，叫做困难难度（半值）和极难难度（五分之一值）。这两个数值用在相关属性和技能检定上，提前计算出来以方便后续游戏进行。
如果使用骰娘进行游戏的话，骰娘在检定时会自动计算出困难成功等</p>
