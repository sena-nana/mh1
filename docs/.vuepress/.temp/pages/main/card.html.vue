<h3 id="幸运-luck-骰3d6-结果乘5-即是幸运属性" tabindex="-1"><a class="header-anchor" href="#幸运-luck-骰3d6-结果乘5-即是幸运属性" aria-hidden="true">#</a> 幸运（LUCK）：骰3D6，结果乘5，即是幸运属性</h3>
<p>幸运属性用于一些随机事件的检定。如果敌人随机攻击一人，那么可以认定是队伍中幸运最低的魔法少女受到了攻击。</p>
<ul>
<li>0：随时随地处于意外状况之中</li>
<li>15：一直抽到大保底才获得当期UP角色</li>
