<h3 id="生命-hp" tabindex="-1"><a class="header-anchor" href="#生命-hp" aria-hidden="true">#</a> 生命（HP）</h3>
<p>角色的体质/5，向下取整，即是基础生命。除此之外，一些固有技能或者技能也能够提供生命。总生命由基础生命和技能生命相加所得。</p>
<h3 id="魔力-mp" tabindex="-1"><a class="header-anchor" href="#魔力-mp" aria-hidden="true">#</a> 魔力（MP）</h3>
<p>大多数情况下，使用魔法时必须消耗魔力。魔法少女在游戏初始的魔力等于她因果值/5。但是崩坏或者魔女会拥有更高的魔力。</p>
<p>魔力在正常探索中不会恢复，当魔法少女到达了一个相对安全的地方，并进行了较长时间的休息后，可以将其的MP恢复至上限。魔力无法超过魔法少女的魔力上限。</p>
<p>角色可以通过消耗MP恢复自身所受到的伤害，包括HP的损失和身上的伤口。</p>
<p>恢复HP需要消耗等量的MP，恢复轻伤状态需要消耗1MP，而恢复重伤状态需要消耗2MP。</p>
<p>当魔法少女的HP为0时，受到的伤害会直接扣除MP值。若MP此时也降到了0，魔法少女会陷入濒死状态无法行动。当处于濒死状态时受到伤害，将可能直接导致魔法少女死亡（这意味着灵魂宝石无法提供足够的魔力来维持身体恢复），除非魔法少女在此时消耗至少3点san值来恢复魔力。</p>
<p>每1点san恢复2MP，详情见 主界面 → 理智 → 诅咒 。</p>
<h3 id="理智-san" tabindex="-1"><a class="header-anchor" href="#理智-san" aria-hidden="true">#</a> 理智（SAN）</h3>
<p>理智的游戏初始值等于｛100－（因果＋感情）/2｝。计算后出现小数向下取整。</p>
<p>SAN值的数值对于魔法少女是隐藏的，只有主持人知道准确的数值和sc的结果。但是魔法少女可以通过检查灵魂宝石的状态来确认SAN值的大概范围。</p>
<p>灵魂宝石部分详情见下文 灵魂宝石 。</p>
<p>理智部分详情见 主界面 → 理智 。</p>
<h2 id="决定技能并分配技能点" tabindex="-1"><a class="header-anchor" href="#决定技能并分配技能点" aria-hidden="true">#</a> 决定技能并分配技能点</h2>
<p>技能点可以自由分配到想要学习的技能。并不是所有技能都需要分配到技能点，这是不明智的选择。未被分配的技能点将会消失。</p>
<h3 id="生活技能" tabindex="-1"><a class="header-anchor" href="#生活技能" aria-hidden="true">#</a> 生活技能</h3>
<p>生活技能点＝智力x2+记忆x2。生活技能是指阅读，写作，绘画等爱好相关的技能，也包括了计算机操作，烹饪，游泳，侦查等技能。生活技能表现了魔法少女自身拥有得一些技能或者特长。和属性一样，请计算出属性半值和属性五分之一值。生活技能像属性一样同样可以用于检定。</p>
<h3 id="战斗技能" tabindex="-1"><a class="header-anchor" href="#战斗技能" aria-hidden="true">#</a> 战斗技能</h3>
<p>初始战斗技能点=（力量+敏捷）/2。战斗技能是指闪避，投掷，射击等与战斗相关的技能。战斗技能表现了魔法少女在战斗上所积累的经验。和属性一样，请计算出属性半值和属性五分之一值。战斗技能像属性一样同样可以用于检定。游戏中，在获得崩坏认知技能点后，会获得一定的战斗技能技能点。</p>
<h3 id="固有能力" tabindex="-1"><a class="header-anchor" href="#固有能力" aria-hidden="true">#</a> 固有能力</h3>
<p>魔法少女的固有能力与愿望有直接关系。</p>
<p>你可以按照自己提前想好的故事来选择，也可以按照队伍分配等选择好后再考虑愿望。</p>
<p>我们鼓励玩家在创建属于自己的魔法少女时，自行设计固有能力的具体内容，当然不要忘了与你的主持人沟通。</p>
<h3 id="武器" tabindex="-1"><a class="header-anchor" href="#武器" aria-hidden="true">#</a> 武器</h3>
<p>魔法少女可以选择一种武器来使用，不同的武器拥有不同的伤害加成。同时一些魔法也可以结合武器发挥更强的效果。
玩家在游戏中可能获得其他可能可以作为武器的道具，虽然它们也可以对敌人造成伤害，但是魔法加成无法对他们起作用</p>
<h2 id="写出背景" tabindex="-1"><a class="header-anchor" href="#写出背景" aria-hidden="true">#</a> 写出背景</h2>
<p>玩家可以自由在该世界观下想象魔法少女曾经经历的事情。包括重要的人物或物品，所许下的愿望，人际关系以及其他特点。当然，并不是说这些全部都要说明。但是背景设定越完善，魔法少女的形象便会愈加鲜活。也可以在游戏中补充设定，前提是不会惹主持人生气。
一个丰富的背景，可以帮助玩家在游戏中更好的理解扮演角色。并会在游戏过程中，帮助主持人决定剧情发展。在魔法少女陷入崩坏时，关键物品也将从重要之物中选择。
请尽可能使用形容词来丰富你的魔法少女形象。不要忘记给你的魔法少女取一个名字。</p>
<h3 id="灵魂宝石" tabindex="-1"><a class="header-anchor" href="#灵魂宝石" aria-hidden="true">#</a> 灵魂宝石</h3>
<p>玩家需要决定魔法少女的灵魂宝石在身体的哪个位置。明显的位置能够在战斗中方便自己和队友互相检查对方的魔力状态，但同时也更容易被对方发现而成为弱点。</p>
<p>当灵魂宝石位于魔法少女视野内时发生了状态变化，主持人可以直接描述灵魂宝石发生了何种变化而无需玩家主动提出检查宝石。</p>
<p>根据san值占san值最大值百分比的不同，灵魂宝石会有不同的状态</p>
<ul>
<li>50%-100%：清澈</li>
<li>20%-50%：略显浑浊</li>
<li>10%-20%：浑浊</li>
<li>0%-10%：接近黑色</li>
</ul>
<h3 id="人际关系" tabindex="-1"><a class="header-anchor" href="#人际关系" aria-hidden="true">#</a> 人际关系</h3>
<p>重要的人可以是父母，兄弟姐妹，挚友或是恋人，偶像或是擦肩而过的陌生人也可以，完全看你将如何描述这段回忆。重要的人也可以是同时在游戏的其他魔法少女，这也会让游戏变得更加有趣。重要的理由可以是正面的接受过帮助，曾共同度过了一些难关等，也可以是负面的对他们抱有愧疚，寻求复仇等。重要的物品或是地点也同样的道理。</p>
<p>魔法少女可以有最多三个朋友，这些人可以是普通人，也可以是魔法少女。人际关系必须能够符合背景故事，比如一个极其贫穷的角色几乎无法与社会上层的人有交集。在游戏遇到棘手的困难时，玩家可以选择向友人求助，同时过幸运判定。主持人可以对这些友人下刀，比如在幸运判定大失败时得到了友人去世的消息，这会需要进行一个SAN检定（1/1D6）</p>
<h3 id="重要之物" tabindex="-1"><a class="header-anchor" href="#重要之物" aria-hidden="true">#</a> 重要之物</h3>
<p>重要之物仅可以选择一个，这通常是角色最重视的，在关键时刻也可能救角色一命。所选定的这些元素，将不会被主持人更改，只有发生了会导致该事物被牵扯进的事情，或是玩家主动要求时，主持人可以对这些进行调整。当进行这些调整时玩家有一次判定机会来拯救这个事物，若失败则需要进行SAN检定（1/1D10）</p>
<h3 id="固有能力-1" tabindex="-1"><a class="header-anchor" href="#固有能力-1" aria-hidden="true">#</a> 固有能力</h3>
<p>固有能力是随着魔法少女的愿望所产生的，不会被debuff影响，除非魔法少女开始怀疑或后悔自己的愿望。当玩家决定了魔法少女的愿望后，将和主持人一起从固有技能列表中选择一个最符合愿望描述的固有能力。</p>
<h3 id="愿望" tabindex="-1"><a class="header-anchor" href="#愿望" aria-hidden="true">#</a> 愿望</h3>
<p>愿望是魔法少女背景故事的总结和冲突的爆发点。这一项无论如何都无法被更改，也是支持魔法少女活动的信念。即便忘记为何许愿，魔法少女也会为了愿望本身而努力。如果魔法少女开始对自己的愿望感到厌恶和后悔，可能会无法使用自己的固有技能。</p>
<h3 id="崩坏" tabindex="-1"><a class="header-anchor" href="#崩坏" aria-hidden="true">#</a> 崩坏</h3>
<p>崩坏是魔法少女陷入绝望时的理想空间，是一种不完全的结界。处于这种结界中时，现实中的场景会受到魔力的影响。玩家应当在此时描述自己的魔法少女在发动崩坏时会发生什么，这种影响应当符合魔法少女的愿望和能力。
当玩家的理智降至0时，无论当时处于何种状况，都会强制发动崩坏。当理智低于初始值的五分之一时，崩坏也可以由玩家主动要求发动。
崩坏的概念会在“崩坏”一章中具体描述</p>
