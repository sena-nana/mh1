<template><div><p>在魔法崩坏中，每位玩家都要制作一个魔法少女角色，这是玩家在魔法崩坏的世界观下活动的化身，是四处探索解决事件的化身。
作为队伍来讲，魔法少女们各有丰富多彩的背景和千奇百怪的技能－－不同的技能将会为队伍带来新的可能性
本章提供创建魔法少女的规则和思路，在本章结尾，我们也提供了一些预置的角色可供参考。
赋予魔法少女以生命是莫大的乐趣。这个规则会告诉你如何骰出你的属性，然后选择爱好和技能，最后写出个人经历。在开始魔法崩坏前，玩家们和主持人一起骰出各自的属性，以便每个人都可以很好地融入这个队伍。因为在之后的活动中，不同的玩家可以发挥自己的长处，选择合适的技能，来让队伍的整体技能最大化</p>
<h1 id="决定属性" tabindex="-1"><a class="header-anchor" href="#决定属性" aria-hidden="true">#</a> 决定属性</h1>
<p>魔法少女拥有10项基本的属性，在游戏中，每种属性都表现了魔法少女的一面——记忆力协调性等。这些属性数字决定了魔法少女的相关才能和游戏中的建议扮演方向</p>
<p>属性具体有：<strong>力量、体质、敏捷、智力、因果、感情、记忆、幸运、体型、魅力</strong>
属性值的决定通常随机决定——通过使用三枚6面骰子（3D6）或者使用两枚6面骰子再加6（2D6+6）。将骰子结果相加后，再乘以5，可以得出一个固定的百分比数。这就是你的最初属性，会在15％到90％之间
一般而言，可以认为魔法少女的身体能力远超常人，比如在临近的大楼之间跳跃对于魔法少女来说是很轻松的事情。但是无法做到超出物理规律的事情</p>
<h2 id="投掷属性" tabindex="-1"><a class="header-anchor" href="#投掷属性" aria-hidden="true">#</a> 投掷属性</h2>
<h3 id="力量-str-骰3d6-结果乘5-即是力量属性" tabindex="-1"><a class="header-anchor" href="#力量-str-骰3d6-结果乘5-即是力量属性" aria-hidden="true">#</a> 力量（STR）：骰3D6，结果乘5，即是力量属性</h3>
<p>力量实际指在魔力加持下魔法少女的力量属性。力量越高，魔法少女就能举起更重的东西或是打碎更加坚硬的物体。该属性会决定魔法少女在近战中造成的伤害。力量降低为0时，虽然有运动技巧可以免受运动造成的伤害，仅论身体技能将和普通的柔弱少女无异</p>
<ul>
<li>0：柔弱，和普通的柔弱少女无异，甚至可能会被轻松推倒</li>
<li>15：正常女生的力量</li>
<li>50：可以轻松达到运动员爆发时的技能</li>
<li>70：可以徒手破坏常见的钢制品</li>
<li>90：全力一拳相当于失控汽车的撞击</li>
</ul>
<h3 id="体质-con-骰3d6-结果乘5-即是体质属性" tabindex="-1"><a class="header-anchor" href="#体质-con-骰3d6-结果乘5-即是体质属性" aria-hidden="true">#</a> 体质（CON)：骰3D6，结果乘5，即是体质属性</h3>
<p>体质实际上指魔法少女在魔力加持下的身体素质。体质和魔法少女受异常状态干扰的技能挂钩。高体质的魔法少女会拥有更高的生命——能承受更多的伤害和攻击。严重的受伤或是魔法干扰可能会降低该属性。而当体质降为0时，意味着任何的魔力扰动都会影响魔法少女。一般来说，魔法少女并不会感染普通人的疾病</p>
<ul>
<li>0：弱不禁风，仅仅能够活动而已</li>
<li>15：有最底线的抵抗力，和普通人相差无几</li>
<li>50：可以抵抗住拳王的攻击</li>
<li>70：可以徒手挡住旋转挥舞的柴刀</li>
