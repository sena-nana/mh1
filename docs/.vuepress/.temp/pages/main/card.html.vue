<h3 id="智力-int-骰2d6-6-结果乘5-即是智力属性" tabindex="-1"><a class="header-anchor" href="#智力-int-骰2d6-6-结果乘5-即是智力属性" aria-hidden="true">#</a> 智力（INT）：骰2D6＋6，结果乘5，即是智力属性</h3>
<p>智力表示魔法少女学习力，理解力，信息分析技能，解密技能等水平。智力降为0的魔法少女会无法思考，甚至无法留下这段时间的记忆。智力方面，魔法少女保持了契约前的水平。智力可以增加魔法少女的生活技能点的数量（INT x 2）。智力同样影响着灵感检定和智力检定。如果受到魔力干扰造成头晕等情况，智力也会受到干扰，但在解除后也会恢复</p>
<ul>
<li>0：植物人，无法思考也无法记忆</li>
<li>15：五八三十五</li>
<li>50：普通人水平，能够在中等水平的学校上保持中游</li>
<li>70：一般人眼中的天才</li>
<li>90：能够掌握多门语言或在多个领域达到专家水平，不可思议的人物</li>
</ul>
<h3 id="因果-pow-骰3d6-结果乘5-即是因果属性" tabindex="-1"><a class="header-anchor" href="#因果-pow-骰3d6-结果乘5-即是因果属性" aria-hidden="true">#</a> 因果（POW）：骰3D6，结果乘5，即是因果属性</h3>
<p>因果表示魔法少女魔力的资质；因果越高，魔法少女的魔法总量越高。因果降为0时魔法少女将无法使用魔力。一般来说，因果值在游戏开始后就不会变动，但是可能会有道具设施等能够对因果造成影响。
因果同样决定了魔法少女的魔力。魔力可以在游戏中消费并回复。魔力最大值等于因果/5</p>
<ul>
<li>0：没有丝毫的魔法潜能，这样的人没有被签约的可能</li>
<li>15：有微弱的魔法潜能，能够拥有简单的魔法技能</li>
<li>50：会被QB主动邀请的程度，普通魔法少女的水平</li>
<li>70：魔法方面有着一定天赋，即便没有成为魔法少女，也能微弱的感受到魔力</li>
<li>90：QB会不顾一切地邀请，在普通人眼里是有着很强灵感的人</li>
</ul>
<h3 id="感情-emo-骰3d6-结果乘5-即是感情属性" tabindex="-1"><a class="header-anchor" href="#感情-emo-骰3d6-结果乘5-即是感情属性" aria-hidden="true">#</a> 感情（EMO）：骰3D6，结果乘5，即是感情属性</h3>
<p>感情表示魔法少女的情感丰富程度，这也反应到了魔法的输出技能；感情越高，魔法少女能够在短时间内输出更多的魔力，魔法少女在每轮战斗中最多输出感情值/5的魔力，否则可能会导致灵魂宝石不稳定而碎裂。感情降为0时魔法少女会无法使用魔力。一般来说，感情值在游戏开始后就不会变动，但是发生的一些重大事情可能会改变感情属性。在使用coc骰娘时，SAN值检定所使用的数值为（100-EMO）</p>
<ul>
<li>0：内心毫无波动，仿佛是人偶一样</li>
<li>15：有些冷酷的感觉，看似不为感情所动，内心还是会有喜怒哀乐</li>
<li>50：理性感性参半</li>
<li>70：丰富的感情能够感染周围的人</li>
<li>90：感情过于丰富，有时甚至会变得冲动</li>
