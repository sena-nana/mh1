<h3 id="记忆-mem-骰3d6-结果乘5-即是记忆属性" tabindex="-1"><a class="header-anchor" href="#记忆-mem-骰3d6-结果乘5-即是记忆属性" aria-hidden="true">#</a> 记忆（MEM）：骰3D6，结果乘5，即是记忆属性</h3>
<p>记忆属性是魔法少女记忆能力的量化，它包含了魔法少女的知识量。记忆表示魔法少女保持的信息数量，而非随机应变的技能。记忆为0的魔法少女可能遭受过严重的失忆以至于对普通人的世界一无所知，这会使得她们很容易接受到错误的信息而受到欺骗。记忆决定了魔法少女初始的生活技能点，在进行知识骰时也会用到记忆属性
虽然魔法少女通常在20岁以下，但也有可能具有70以上的记忆属性，这说明她十分聪慧而在很小的时候进入大学，或是具有过目不忘的能力</p>
<ul>
<li>0：刚刚从严重失忆中清醒</li>
<li>15：经常会忘记事情</li>
<li>50：平常人的记忆能力</li>
<li>70：记忆力很好，具有大学级别的知识量</li>
<li>90：过目不忘，即便是很小的细节也能够记得很清楚</li>
