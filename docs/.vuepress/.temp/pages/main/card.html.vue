<h3 id="敏捷-dex-骰3d6-结果乘5-即是敏捷属性" tabindex="-1"><a class="header-anchor" href="#敏捷-dex-骰3d6-结果乘5-即是敏捷属性" aria-hidden="true">#</a> 敏捷（DEX）：骰3D6，结果乘5，即是敏捷属性</h3>
<p>高敏捷的魔法少女更为敏捷灵活，肉体更加柔韧。魔法少女拥有比普通人更快的反应速度，但是身体柔韧和常人相似。敏捷检定可以帮助你在坠落中抓住支撑，高速穿越敌人，或是做到一些纤细的行动。敏捷降为0的魔法少女将无法移动身体。在战斗中，高敏捷的魔法少女可以优先行动。</p>
<ul>
<li>0：没有协助无法移动身体</li>
<li>15：和普通人没有什么区别</li>
<li>50：已经是普通职业运动员级别的速度</li>
<li>70：反应快速、手接子弹，跑步随便突破世界纪录不在话下</li>
<li>90：高速而灵活，超过了绝大部分人，可以和汽车赛跑</li>
