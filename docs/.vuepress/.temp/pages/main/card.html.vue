<h3 id="体型-size-骰3d6-结果乘5-即是体型属性" tabindex="-1"><a class="header-anchor" href="#体型-size-骰3d6-结果乘5-即是体型属性" aria-hidden="true">#</a> 体型（SIZE）：骰3D6，结果乘5，即是体型属性</h3>
<p>体型属性用于表示魔法少女的身体大小。娇小的魔法少女可以探索一些缝隙，也能够在战斗中找到更多掩体。高大的魔法少女可以发现高处的线索，近战中也会有一定优势。体型是包括身高，体重，胸围等数值的抽象化属性，玩家可以在符合数值范围内细化自己的角色形象。</p>
<ul>
<li>0：一米吧</li>
<li>15：会被错认为小学生的萝莉体型</li>
<li>50：属于十五六岁少女的平均身高</li>
<li>70：大概是御姐身高</li>
<li>90：已经可以用高大来形容</li>
