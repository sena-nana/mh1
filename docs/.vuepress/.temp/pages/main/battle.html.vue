<p>当玩家魔法少女之间产生冲突时，玩家之间可主动提出或者由kp宣言双方对抗的开始，kp依照情况以及玩家讨论选择一下三种形式进行对抗：</p>
<h3 id="切磋" tabindex="-1"><a class="header-anchor" href="#切磋" aria-hidden="true">#</a> 切磋</h3>
<p>对抗双方开始战斗，将一方某一人生命削减到三分之一即结束，随后生命恢复切磋开始前的生命值，而后失败方失去1d2生命</p>
<h3 id="决斗" tabindex="-1"><a class="header-anchor" href="#决斗" aria-hidden="true">#</a> 决斗</h3>
<p>对抗双方开始战斗，某一方全员生命归零即结束</p>
