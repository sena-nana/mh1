<h2 id="受伤" tabindex="-1"><a class="header-anchor" href="#受伤" aria-hidden="true">#</a> 受伤</h2>
<p>当魔法少女受到超过自身HP一半的刃形伤害时会受轻伤，受到超过自身HP上限的刃形伤害时会受到重伤</p>
<p>当魔法少女无论因何种伤害HP归0时都会受到轻伤</p>
<p>魔法少女受轻伤时，移动每DP所能移动的距离减半，同时无法执行单个消耗4DP以上的行动</p>
<p>魔法少女受重伤时，无法移动，无法行动，在得到治疗之前会一直躺在地上</p>
<p>伤口可以通过消耗MP恢复，也可以在战斗之外通过战斗技能中的“伤口处理”恢复。在魔法少女到达一个相对安全的地方并经过了长时间的休息后，轻伤也会自然恢复，重伤则会恢复为轻伤。</p>
