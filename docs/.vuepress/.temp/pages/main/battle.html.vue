<h2 id="被动行动" tabindex="-1"><a class="header-anchor" href="#被动行动" aria-hidden="true">#</a> 被动行动</h2>
<p>玩家有三种选择——格挡、闪避或是反击，若无DP可消耗则相当于魔法少女精疲力竭面对攻击无法活动。</p>
<p>格挡使用坚韧性属性，投掷1dDEF为格挡值，受到的最终伤害为（造成伤害 - 格挡值），最低为0，本次对方攻击视为命中。格挡行动不需要消耗DP。</p>
<p>闪避使用战斗技能-闪避属性检定，若对抗检定成功，则视为对方没有命中。闪避行动需要消耗1DP</p>
<p>反击使用力量属性，双方进行对抗检定，当对抗检定成功时，视为不受到此次伤害并对对方造成一次攻击，否则视为反击失败，魔法少女会受到对方攻击造成的伤害。无论反击是否成功，均视为对方的攻击命中。使用远程武器或者被远程武器攻击无法使用反击。使用反击行动需要消耗武器攻击的DP/2</p>
