<h3 id="注意回避" tabindex="-1"><a class="header-anchor" href="#注意回避" aria-hidden="true">#</a> 注意回避</h3>
<p>当声明这种行动时，魔法少女将专注于回避攻击。这将消耗2点DP，并且无法执行其他被动行动。直到你下一回合开始之前，所有的闪避行动都可以获得一个奖励骰</p>
<h3 id="注意格挡" tabindex="-1"><a class="header-anchor" href="#注意格挡" aria-hidden="true">#</a> 注意格挡</h3>
<p>当声明这种行动时，魔法少女将专注于格挡攻击。这将消耗2点DP，并且无法执行其他被动行动。直到你下一回合开始之前，所有的格挡行动都可以获得一个1d3的加值</p>
<h3 id="预备行动" tabindex="-1"><a class="header-anchor" href="#预备行动" aria-hidden="true">#</a> 预备行动</h3>
<p>如果玩家想要等待时机行动，而这个时机可能并不在自己的回合中产生，那么玩家可以声明预备行动，此时魔法少女会专注于等待这个时机来进行预先声明的行动。</p>
<p>声明这种行动需要消耗1DP，并且在时机触发时需要消耗行动所对应的DP量来完成这个行动，如果没有足够的DP会认为行动失败</p>
