<h1 id="战斗轮" tabindex="-1"><a class="header-anchor" href="#战斗轮" aria-hidden="true">#</a> 战斗轮</h1>
<p>战斗轮代表着战斗的过程，战斗轮遵循回合制的基本规则，行动根据预先设定的顺序依次进行</p>
<ol>
<li>判定突袭。kp判定战斗遭遇参与者中是否有人遭受突袭。</li>
<li>决定位置。kp决定好所有角色和怪物的位置。即kp以少女们的行进方向，及其在房间或其他地点的具体位置为基础，再确定其敌对者在哪（距离的远近和具体位置）。</li>
<li>掷先攻。战斗遭遇的每位参与者骰出<strong>先攻</strong>，并依此决定各参战者回合的顺序。</li>
<li>执行回合。战斗的每位参与者按照先攻序列进行其战斗回合。</li>
<li>开始新一轮。当所有战斗参与者完成其回合后，该轮结束。重复步骤4直至战斗结束。</li>
</ol>
<p>战斗中，如果没有特殊说明一个战斗者的行动消耗的时间约为6s</p>
<p>在非战斗的情况下，如果没有特殊说明，游戏时间与现实时间流速相当</p>
<h2 id="先攻" tabindex="-1"><a class="header-anchor" href="#先攻" aria-hidden="true">#</a> 先攻</h2>
<p>先攻”决定了战斗时各人回合的顺序。战斗开始时，每名参战者都要掷一次敏捷检定作为自己的先攻，并依此决定自己在先攻序列中的位置。另外，kp还可以自行判断一群相同的生物是否共用同一个检定作为其先攻，并让这群生物在同一时间行动。</p>
<p>kp将参战者们的该次敏捷检定结果由高至低顺序排列，作为各人在战斗中的行动顺序，即为<strong>先攻序列</strong>。该先攻序列在战斗轮更替时一直维持不变。除非有特殊情形。</p>
<p>用作先攻的敏捷检定其数值相同时，若对象都是kp控制的生物，则其具体顺序由kp决定；若对象都是玩家角色，则其具体顺序由玩家们自行决定；而如果是DM的角色与玩家角色构成平手，则其具体顺序依然由kp决定。kp也可以选择让平手的角色各掷一次d100，再让得值高者获得先手。</p>
<p>受伤的角色在先攻序列中的位置会被临时置为最后，直至其伤口恢复</p>
<h2 id="你的回合" tabindex="-1"><a class="header-anchor" href="#你的回合" aria-hidden="true">#</a> 你的回合</h2>
<p>在你自己的回合里，你可以依照自己的速度<strong>移动一定</strong>距离，或执行行动（将消耗DP）。你可以自行决定是先移动还是先执行决策。另外，你的速度（有时会称为步行速度）则记录在你的角色卡中。</p>
<p>你可以在自己回合内放弃移动，或放弃执行动作，甚至可以在自己回合内什么都不做。</p>
<p>你在自己回合内除了移动和执行动作外，还可以进行各种花式的行为如下举例。</p>
<ul>
<li>能够进行沟通时，你在自已回合中随时都可以透过短句和手势与他人进行沟通。</li>
<li>你在移动或执行动作时，也可以跟至多一个物件或环境特性进行自由互动。例如，你可以在移动接近敌人的途中打开一扇门，或者在攻击动作进行时拔出武器。</li>
<li>如果你想跟另一个物件互动，则需要使用你的动作。某些魔法物品或特殊物件也会需要用一个动作来使用，而这通常会在它们的文字描述中提及。</li>
<li>进行比较费神或不寻常的活动时，主持人也可能会要求你用一个动作。例如，主持人可以合理地要求你用动作去开启一扇卡死的门，或者操作场地中的大型机械</li>
</ul>
<h2 id="应急反应" tabindex="-1"><a class="header-anchor" href="#应急反应" aria-hidden="true">#</a> 应急反应</h2>
<p>在某些特殊情况下，玩家可以向主持人申请进行应急反应。应急反应是对时间触发瞬间的一个即时回应，可以在其他人的回合中发生。从你执行一个反应开始，直至你下一个回合开始前，都不能再进行应急反应。如果你的应急反应打断了其他人的回合行动，那么在应急反应结束后，继续那个人的回合行动。</p>
<p>在应急反应中，行动会消耗双倍DP，在玩家执行了应急反应之后的下个回合不会自然恢复DP</p>
<h3 id="保护同伴" tabindex="-1"><a class="header-anchor" href="#保护同伴" aria-hidden="true">#</a> 保护同伴</h3>
<p>当两个魔法少女处在相同位置时，其中一个魔法少女被攻击时，另一个魔法少女可以申请应急反应来代替该魔法少女成为被攻击目标，在这之后，保护同伴的魔法少女可以使用被动行动来应对攻击。如果是AOE的攻击，保护同伴的行动同样有效但是会承受1.5人份的伤害（向下取整）</p>
<h2 id="dp系统" tabindex="-1"><a class="header-anchor" href="#dp系统" aria-hidden="true">#</a> DP系统</h2>
<p><strong>行动值（DP）行动值影响着魔法少女战斗中能够进行的操作数量，每个行动都会消耗一定的DP。</strong></p>
<p><strong>当回合没有消耗完的DP会积累到下一回合。</strong></p>
<p><strong>战斗开始时，每个玩家拥有8点DP+特殊情况给予的DP。如果是突袭，突袭成功时，突袭方获得2DP</strong></p>
<p><strong>每个战斗轮开始时，会恢复所有玩家和敌人4点DP+特殊情况给予的DP，每个玩家和敌人拥有的DP无法超过15点。（可以通过固有能力提高玩家DP上限）</strong></p>
<p>战斗轮结束时，清空所有人的DP，并在下一个战斗轮开始时重新计算</p>
<ul>
<li>小行动：小行动消耗1DP，一般指不需要大幅度移动身体的动作</li>
<li>标准行动：标准行动消耗2DP，一般指姿态调整等需要大幅度移动身体的动作</li>
<li>大行动：大行动消耗4DP，一般指需要大幅度活动身体，且需要消耗一定的体力来完成的动作</li>
</ul>
<p>在战斗中使用生活技能或者战斗技能时，主持人可以通过该技能的动作幅度来动态判断该行动应消耗多少DP</p>
