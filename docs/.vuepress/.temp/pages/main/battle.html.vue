</ul>
<p>位置移动按照消耗的DP算作不同的行动等级，回合内所有移动记作一次行动。</p>
<p>魔法少女所能携带的物体总重量不超过自己力量属性的两倍。当超过这个值时，魔法少女的移动消耗会翻倍，并且无法在危险地形中移动，而超过魔法少女力量属性的三倍时，魔法少女将无法进行任何行动。</p>
