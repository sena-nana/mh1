<h3 id="使用能力" tabindex="-1"><a class="header-anchor" href="#使用能力" aria-hidden="true">#</a> 使用能力</h3>
<p>魔法少女消耗自身能力所需要的DP和MP，释放一次魔法。一般而言，dp代表了战斗中内在的专注程度，精神好坏等情况，因此华丽而令敌人胆寒的法术需要更多的dp和mp来释放</p>
<h3 id="移动" tabindex="-1"><a class="header-anchor" href="#移动" aria-hidden="true">#</a> 移动</h3>
