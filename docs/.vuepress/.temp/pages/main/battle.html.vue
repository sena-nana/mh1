<p>当武器命中检定极难成功时，造成的伤害跳过判定，直接按照伤害的最高值计算，并使对方受到轻伤，如果此次攻击本身就带有轻伤效果，则提高该效果为重伤。</p>
<p>当武器命中检定大成功时，在按照极难成功的基础上，还会额外造成1d（DAM/2）的伤害</p>
