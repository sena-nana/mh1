<h3 id="伤害计算公式" tabindex="-1"><a class="header-anchor" href="#伤害计算公式" aria-hidden="true">#</a> 伤害计算公式</h3>
<p>最终伤害=（基础伤害*重量加成+重量加值）*附加属性+附加伤害+伤害加值</p>
