<h3 id="第一部分-月台四周" tabindex="-1"><a class="header-anchor" href="#第一部分-月台四周" aria-hidden="true">#</a> 第一部分：月台四周</h3>
<p>魔法少女们在月台相遇，这个时候QB可以让玩家们互相交换情报。在结界内部，手机是没有信号的。此时环境中下着小雨，但是地面上却并没有水洼反而是像雪一样铺着薄薄的一层。</p>
<ul>
<li><strong>雨</strong> 在结界中的雨点实际上是诸如玩偶、可丽饼之类的，两个少女曾经回忆中出现的物品，所以并不会将玩家打湿，也不会像水洼一样聚集。
<ul>
<li>在游戏刚开始时，由于雨点还是非常小的状态，除非是具有微视能力的魔法少女，不然无法辨认出具体的物体，只能判断成粉末状的固体。</li>
<li>随着游戏进行，雨点会逐渐变大，并逐渐变成玩家能够肉眼判断的大小甚至更大，并堆积在荒野上。</li>
</ul>
</li>
<li><strong>月台</strong> 的结构非常简单，仅有几个长椅，一个站牌，数盏路灯便空无他物。而车站的四周是无比空旷的荒野，更远处则被浓雾所遮盖。
<ul>
<li><strong>长椅</strong> 长椅似乎并没有特别的地方。但是对背景1或者对车站熟悉的玩家可以认出这并不是车站的长椅样式。玩家可以通过检定，或是具有熟悉商业街的背景，来认出这个长椅的样式是商业街的款式。
<ul>
<li>长椅的款式与之后出现的照片中的长椅款式是一致的</li>
</ul>
</li>
<li><strong>站牌</strong> 站牌已经非常老旧，并且处于灯光的暗处。上面带有红褐色的印记（血液）遮挡了大部分文字，但是中间的站台名可以轻易辨认 <strong>如月车站</strong>
<ul>
<li>如果玩家仔细调查站牌，会很容易发现印记实际是粘稠的液体，但是似乎可以被擦除掉。</li>
<li>如果玩家想要徒手擦掉印记，则液体会粘在玩家的手上。并在玩家回到灯光下时注意到这件事。此时玩家会立即察觉到一股铁锈味并需要进行一个灵感检定
<ul>
<li>当灵感检定失败时，玩家仅会觉得这是令人作呕的不明液体，并会因为自己想象力造成的不安损失0/1D3的SAN值</li>
<li>当灵感检定成功时，玩家会察觉到这是血的事实，并会损失1D2/1D5的SAN值。</li>
</ul>
</li>
<li>当印记被擦除掉时，玩家就可以辨认出这是列车的运行时间，首发车的位置写着三年前的九月十五日，而末班车的位置则写着六月九日。九月15日是遇到莲实的日子，也是之后行李箱的密码
<ul>
<li>玩家可以自行判断日期的含义，也可以通过记忆检定、灵感检定由主持人提供线索</li>
<li>六月九日是附近一所高中的毕业典礼的日子
<ul>
<li>如果玩家察觉到了这点：如果六月九日指的是高中毕业，那么九月十五日则是入学后不久</li>
</ul>
</li>
<li>六月九日是朝北奈车站曾经发生事故的日子（背景2限定）</li>
</ul>
</li>
</ul>
</li>
<li><strong>路灯</strong> 路灯并没有什么异常，似乎就是普通的站台路灯。只是似乎有受过撞击的痕迹</li>
<li>当玩家们想要穿越浓雾时，便会听到远处传来列车的声音，并听到列车即将到站的报站声，如果玩家没有立即回到站台，则会被飞驰而来的列车撞到，损失1D6的HP.在那之后列车会停靠在站台</li>
<li>当玩家探索完成后，主持人便可以让列车从浓雾中驶入，停靠在站台
<ul>
<li>玩家们会注意到地面上什么也没有的地方出现了铁轨，列车随后驶来</li>
<li>如果玩家长时间不上车，车站的灯光就会闪烁，此时SAN值低于一半的玩家都会在闪烁时看到一个少女被列车撞死的景象，并损失1D4/1D8的SAN值。随后列车开始鸣笛，准备发车。具体的时长可以根据玩家的反映来调整</li>
<li>如果玩家们执意不上车，则可以直接跳到最后一个场景直接面对崩坏化身。</li>
</ul>
</li>
</ul>
</li>
</ul>
<h3 id="第二部分-列车一车厢" tabindex="-1"><a class="header-anchor" href="#第二部分-列车一车厢" aria-hidden="true">#</a> 第二部分：列车一车厢</h3>
<p>列车车厢是普通的列车布置，但是两侧连通的车门是锁死的。车窗的外面仍在下着“雨”，地面上是荒野，远处仍是浓雾</p>
<p>在座位下面有一个<strong>行李箱</strong></p>
<p>一侧车门的外面有一个<strong>少女</strong>（也可以是和人设图有一定差别的幼女），是对于莲实的记忆在崩坏中的魔力的具现化，也是会影响结局的重要因素。在玩家进入车厢后就在车门外偷偷观察（当然会避开玩家探索的目光，除非被反侦察到，由于车厢的隔音，也不会被<strong>聆听</strong>所察觉到），当玩家探索进入一阶段后就开始敲玻璃吸引玩家的注意力。而在她的指引下，玩家可以发现车门上的紧急按钮，并打开通往二车厢的门。</p>
<ul>
<li><strong>行李箱</strong> 行李箱的密码是915，当然也可以通过暴力打开。当玩家打开行李箱时，整个车厢突然会堆满相片，而行李箱中是一个手机。
<ul>
<li><strong>相片</strong> 相片中全部是如月和莲实二人的合照，但是玩家并不知道二人的长相，可以按照二人的设定图来描述。
<ul>
<li>所有照片中，莲实的位置都变得模糊不清，当玩家仔细观察时，会进行一个灵感的判定。一旦判定过在查看其他照片时就不会再触发判定。
<ul>
<li>判定成功时，玩家会发现莲实的位置变得支离破碎，身体以不正常的方式扭曲着。但是大脑却认为这是一个正常且完整的人，虽然无法辨认相貌但是可以识别出照片中的动作。玩家需要进行一个0/1D3的SC。</li>
<li>将<strong>少女</strong>带至一车厢后，判定成功的玩家能够辨认出照片中莲实的大概相貌</li>
<li>判定失败时，玩家只会觉得照片上模模糊糊，无法辨认</li>
</ul>
</li>
<li>照片上的内容都是二人的日常互动，例如一起吃可丽饼、互相吃对方的冰淇淋、睡衣派对、拍大头贴……总之是各种贴贴的美少女互动行为，只要玩家一直看可以一直编</li>
</ul>
</li>
<li><strong>手机</strong> 手机的所有界面都一直在抖动，所有文字都模糊不清，屏幕时而变色时而出现条纹。玩家会明显感觉继续看下去会有不好的事情发生，但是能察觉到手机上有明显的魔力波动。当玩家选择继续观看的时候，所有看着屏幕的玩家对于一车厢的观察会被回忆·一车厢的场景强制替换，并因为辨认扭曲怪异的界面而损失1/1D3的SAN值。
<ul>
<li><strong>回忆·一车厢</strong> 回忆中的车厢看起来像是一个商业街的小巷，座椅被商业街的座椅替换，而车厢则变成了墙壁。只是二车厢另一边的车门，此时变成了一个甜品店。手机上的文字也可以辨认了
<ul>
<li><strong>手机</strong> 手机打开后会显示一个甜品店的优惠券</li>
<li><strong>甜品店</strong> 甜品店的店员外貌并不重要，可以随意描述。店员会正常与玩家交流，但是内容都是建立在现实中的甜品店基础上的。不过根据玩家问的问题还是会有一些特殊的回答作为线索：
<ul>
<li>与店员对话，向她展示手机上的优惠券，她会送给玩家一个<strong>可丽饼</strong>。将可丽饼给少女吃可以让少女回忆起一部分自己，使少女的状态恢复一次。少女会觉得是令人怀念的味道，曾经和谁一起吃过。毕竟是魔力的聚合体，如果由玩家吃掉的话……魔法少女会肚子痛吗</li>
<li>“因为我知道自己是甜品店的店员，我才是甜品店的店员”或者类似的回答，作为提示让玩家帮助莲实回忆自己。</li>
</ul>
</li>
<li>当玩家想要离开甜品点时，回忆就会消失回到一车厢的景象，手机会黑屏</li>
</ul>
</li>
</ul>
</li>
</ul>
</li>
<li><strong>少女</strong> 少女不记得自己是谁，也不知道这里是哪里。但是会告诉玩家如果她想不起来自己是谁，会有不好的事发生。在玩家打开车门后，会请求跟随玩家一起行动。当玩家获得线索时，少女可能会有所反映，也可能什么也不说，根据主持人的想法来自由发挥。
<ul>
<li>
<p>玩家每经过一段时间的调查（每次大概20min？具体由主持人把握），都会使得少女身体的一部分变得视觉上破碎。影响少女的行动并扣除3HP同时会让少女的精神变得更加混乱。</p>
<ul>
<li>这是一个渐变的过程，玩家可以看到破碎的位置在逐渐扩展。只是方便记录使用次数来计算。虽然设想上是像素故障那种破碎，不过具体如何描述可以按照主持人风格。</li>
<li>而四次后，少女就会失去理智，对玩家发起攻击，失去理智后破碎的身体仍然可以行动。当玩家告诉少女她自己是谁时，就不再会触发新的破碎，同时精神回复。但是已经破碎的肢体不会复原。</li>
<li>当少女意识到自己的身份，就会请求玩家把她带到如月身旁</li>
</ul>
</li>
<li>
<p>少女的属性可以现车一个属性卡，也可以使用下方的随便车的卡，少女按照普通人来判定所以力量体质等会有惩罚骰。</p>
