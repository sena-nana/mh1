<li>进入回忆的玩家可以和回忆中的物体互动。而此时，没有进入回忆的玩家仍然看到原本的场景。</li>
<li>玩家仍可以互相看到并互相交流</li>
<li>外界的玩家的攻击无法命中回忆中的敌人，回忆中的敌人也无法攻击到外界的玩家，但是回忆和外界对场景造成的更改同样会作用到另一个空间。</li>
<li>回忆中可能会有特殊的线索和道具，也可能会有敌人。触发回忆时也可能会对结界造成一定的影响。</li>
<li>一版规则中关于回忆战斗胜利和失败的描述和特殊战斗规则废弃。</li>
