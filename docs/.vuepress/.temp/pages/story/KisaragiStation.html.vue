<h3 id="崩坏背景" tabindex="-1"><a class="header-anchor" href="#崩坏背景" aria-hidden="true">#</a> 崩坏背景</h3>
<p>雨宫如月曾因家庭的工作原因，一段时间内四处转学。而稍稍安定下来后，又会转到其他学校。这使得她不得不经常和朋友分别。于是她向许愿想要在高中有一个能够一直陪伴她的朋友。于是第二天，爸爸向她介绍了作为搭档女儿的莲实。莲实之前也随着家里四处转学，但是很快变得内向。于是如月和莲实很快形影不分。但是在毕业的第二天，莲实家里突然搬家，莲实没来得及和如月道别，便前往了车站。如月接到消息后，迅速跑到车站，看到莲实的时候，她飞奔着抱了上去。但是莲实脚下不稳，带着如月向后栽下了月台，而此时，列车刚好到站……作为魔法少女的她醒来后，但是看到莲实浑身带血的样子，陷入了崩坏……但是实际上，二人都互相隐瞒着魔法少女的身份。莲实醒来后，找不到如月的身影，被送入了医院。之后无论如何也联系不上如月，以为如月也已经离开……</p>
